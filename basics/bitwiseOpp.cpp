    cout<<(5&6)<<endl; //5=101  and 6=110  so taking and will give us 100 =4
    cout<<(5|6)<<endl;//again if we take or of 5 and 6 bits it will give 7
    cout<<(5^6)<<endl; //xor , it will give us 3
    cout<<(2>>1)<<endl; //2 =1,0 bits right shift of bit 2 place it will give us 1
    cout<<(2<<1)<<endl; //this will give us 4 due to one bit shift to left making it 100 =4 
    
